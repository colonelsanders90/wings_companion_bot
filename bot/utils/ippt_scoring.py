# Human-readable age-group labels
_AGE_GROUP_LABELS: list[str] = [
    "< 22", "22–24", "25–27", "28–30", "31–33", "34–36",
    "37–39", "40–42", "43–45", "46–48", "49–51", "52–54", "55–57", "58–60",
]


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def get_age_group(age: int) -> int:
    """Map age → age-group index (0–13)."""
    if age < 22:
        return 0
    if age > 60:
        return 13
    return min(13, (age - 22) // 3 + 1)


def get_age_group_label(age: int) -> str:
    return _AGE_GROUP_LABELS[get_age_group(age)]


def get_reps_points(reps: int, age_group: int) -> int:
    """Look up push-up or sit-up points for a given rep count and age group."""
    idx = max(0, min(reps, len(_FEMALE_REPS) - 1))
    return _FEMALE_REPS[idx][age_group]


def get_run_points(run_seconds: int, age_group: int) -> int:
    """Look up run points.  Returns 0 if slower than the last row."""
    for row_time, pts in _FEMALE_RUN:
        if run_seconds <= row_time:
            return pts[age_group]
    return 0


def get_award(total: int) -> str:
    if total >= 85:
        return "🥇 Gold"
    if total >= 75:
        return "🥈 Silver"
    if total >= 51:
        return "✅ Pass"
    return "❌ Fail"


def get_incentive(total: int) -> str:
    if total >= 85:
        return "$300"
    if total >= 75:
        return "$200"
    return ""


def points_to_next_award(total: int) -> tuple[str, int] | None:
    """Return (next_award_name, points_needed) or None if already Gold."""
    for threshold, name in ((85, "Gold 🥇"), (75, "Silver 🥈"), (51, "Pass ✅")):
        if total < threshold:
            return name, threshold - total
    return None


def compute_score(
    age: int,
    pushups: int,
    situps: int,
    run_seconds: int,
) -> dict:
    """Master scoring function.  Returns a dict with all breakdown data."""
    ag = get_age_group(age)
    pu_pts  = get_reps_points(pushups, ag)
    su_pts  = get_reps_points(situps,  ag)
    run_pts = get_run_points(run_seconds, ag)
    total   = pu_pts + su_pts + run_pts
    return {
        "age_group":   get_age_group_label(age),
        "pushup_pts":  pu_pts,
        "situp_pts":   su_pts,
        "run_pts":     run_pts,
        "total":       total,
        "award":       get_award(total),
        "incentive":   get_incentive(total),
        "next_award":  points_to_next_award(total),
    }


def fmt_seconds(seconds: int) -> str:
    """Convert seconds → MM:SS string."""
    return f"{seconds // 60:02d}:{seconds % 60:02d}"
